/* ---- Low-level I2C helpers ----------------------------------------------- */

static void oled_write(const uint8_t *buf, size_t len)
{
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (s_addr << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write(cmd, buf, len, true);
    i2c_master_stop(cmd);
    i2c_master_cmd_begin(I2C_PORT, cmd, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(cmd);
}

static void oled_cmd(uint8_t c)
{
    uint8_t buf[2] = {0x00, c};   /* control byte: Co=0, D/C#=0 → command */
    oled_write(buf, 2);
}

/* ---- Public API ---------------------------------------------------------- */

void ssd1306_init(int sda_pin, int scl_pin, uint8_t i2c_addr)
{
    s_addr = i2c_addr;

    i2c_config_t conf = {
        .mode             = I2C_MODE_MASTER,
        .sda_io_num       = sda_pin,
        .scl_io_num       = scl_pin,
        .sda_pullup_en    = GPIO_PULLUP_ENABLE,
        .scl_pullup_en    = GPIO_PULLUP_ENABLE,
        .master.clk_speed = I2C_FREQ,
    };
    i2c_param_config(I2C_PORT, &conf);
    i2c_driver_install(I2C_PORT, conf.mode, 0, 0, 0);

    /* SSD1306 initialisation sequence */
    const uint8_t init_seq[] = {
        0xAE,                           /* display off */
        0xD5, 0x80,                     /* clock divide / oscillator freq */
        0xA8, (uint8_t)(OLED_HEIGHT-1), /* multiplex ratio */
        0xD3, 0x00,                     /* display offset: 0 */
        0x40,                           /* display start line: 0 */
        0x8D, 0x14,                     /* charge pump ON */
        0x20, 0x00,                     /* memory addressing: horizontal */
        0xA1,                           /* segment remap: col 127 → SEG0 (horizontal flip) */
        0xC8,                           /* COM scan direction: reversed (vertical flip) */
        0xDA, (OLED_HEIGHT > 32 ? (uint8_t)0x12 : (uint8_t)0x02), /* COM pins */
        0x81, 0xCF,                     /* contrast */
        0xD9, 0xF1,                     /* precharge period */
        0xDB, 0x40,                     /* VCOM deselect level */
        0xA4,                           /* resume to RAM content */
        0xA6,                           /* normal (non-inverted) display */
        0xAF,                           /* display on */
    };
    for (size_t i = 0; i < sizeof(init_seq); i++)
        oled_cmd(init_seq[i]);

    ssd1306_clear();
    ssd1306_flush();
}

void ssd1306_clear(void)
{
    memset(s_fb, 0, sizeof(s_fb));
}

void ssd1306_clear_row(int row)
{
    if (row < 0 || row >= OLED_ROWS) return;
    memset(&s_fb[row * OLED_WIDTH], 0, OLED_WIDTH);
}

void ssd1306_flush(void)
{
    oled_cmd(0x21);
    oled_cmd((uint8_t)OLED_COL_OFFSET);
    oled_cmd((uint8_t)(OLED_COL_OFFSET + OLED_WIDTH - 1));
    oled_cmd(0x22); oled_cmd(0); oled_cmd((uint8_t)(OLED_ROWS - 1));

    /* Send framebuffer in 32-byte chunks */
    uint8_t buf[33];
    buf[0] = 0x40;  /* control byte: Co=0, D/C#=1 → data stream */
    int total = OLED_WIDTH * OLED_ROWS;
    for (int i = 0; i < total; i += 32) {
        int chunk = total - i < 32 ? total - i : 32;
        memcpy(buf + 1, s_fb + i, chunk);
        oled_write(buf, 1 + chunk);
    }
}

void ssd1306_putchar(int col, int row, char c)
{
    if (col < 0 || col >= OLED_COLS || row < 0 || row >= OLED_ROWS) return;
    if (c < 32 || c > 127) c = ' ';
    const uint8_t *glyph = s_font8x8[(uint8_t)c - 32];

    /* Map font to SSD1306 page format.
     * With 0xA1 segment remap, GDDRAM col N → physical col (127-N), so we
     * write font column x to base+(7-x) to compensate the horizontal flip. */
    int base = row * OLED_WIDTH + col * 8;
    for (int x = 0; x < 8; x++) {
        uint8_t col_byte = 0;
        for (int y = 0; y < 8; y++) {
            if (glyph[y] & (0x80 >> x))
                col_byte |= (1 << y);
        }
        s_fb[base + (7 - x)] = col_byte;
    }
}

void ssd1306_puts(int col, int row, const char *s)
{
    while (*s && col < OLED_COLS) {
        ssd1306_putchar(col++, row, *s++);
    }
}

void ssd1306_power(bool on)
{
    oled_cmd(on ? 0xAF : 0xAE);
}
